class Employee {
    private String id;
    private String name;
    private double salary;

    public void setID(String id){
        this.id=id;
    }
    public void setNAME(String name){
        this.name=name;
    }
    public void setSALARY(double salary){
        this.salary=salary;
    }
    public String getname(){
        return this.name;

    }
}
